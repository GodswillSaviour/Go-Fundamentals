// My first Go program
// Topic: Variables & Output

package main

import "fmt"

func main() {
	// Declaring a variable to store my name
	var name string = "Godswill"

	// Printing hello world
	fmt.Println("Hello, World!")

	// Using the variable in output with Printf
	fmt.Printf("My name is %s\n", name)

	// Welcome message
	fmt.Println("Welcome to my Go journey!")
}